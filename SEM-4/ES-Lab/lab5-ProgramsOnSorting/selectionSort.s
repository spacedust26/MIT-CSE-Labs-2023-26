;Write an assembly program to sort an array using selection sort
	AREA RESET,DATA,READONLY
	EXPORT __Vectors
__Vectors
	DCD 0X10001000
	DCD Reset_Handler
	ALIGN
	AREA MYCODE, CODE, READONLY
	EXPORT 	Reset_Handler
Reset_Handler
	MOV R4,#0
	MOV R1,#10
	LDR R0,=LIST
	LDR R2,=DST
UP	LDR R3,[R0,R4]
	STR R3,[R2,R4]
	ADD R4,#4
	SUB R1,#01
	CMP R1,#0
	BHI UP
	LDR R0,=DST
	MOV R9,#9
L2	MOV R10,R9
	MOV R1,R0
	MOV R3,R0
	ADD R3,#4
L1	LDR R4,[R3]
	LDR R7,[R1]
	CMP R4,R7
	MOVCC R1,R3
	ADD R3,#4
	SUBS R10,#1
	BNE L1
	LDR R2,[R0]
	LDR R6,[R1]
	STR R2,[R1]
	STR R6,[R0]
	ADD R0,#4
	SUBS R9,#1
	BNE L2
STOP
	B STOP

LIST DCD 10,5,7,8,4,6,9,2,1,3
	AREA MYDATAD,DATA,READWRITE
DST DCD 0
	END