Reset_Handler
	LDR R1,=5
	LDR R2,=9
	LDR R3,=1
	LDR R4,=7
	LDR R5,=3
	LDR R6,=8
	LDR R7,=2
	LDR R8,=0
	LDR R9,=4
	LDR R10,=6
	STMDB R13!,{R1-R10}
	MOV R0,R13
L2	MOV R1,R0
	MOV R3,R0
	ADD R3,#4
	ADD R11,R12,#1
L1	LDM R3,{R4}
	LDM R1,{R7}
	CMP R4,R7
	MOVCC R1,R3
DOWN
	ADD R3,#4
	ADD R11,#1
	CMP R11,#10
	BNE L1
	LDM R0!,{R2}
	LDM R1!,{R6}
	STMDB R1,{R2}
	STMDB R0,{R6}
	ADD R12,#1
	CMP R12,#9
	BNE L2
	LDM R13!,{R1-R10}
STOP
	B STOP
	END